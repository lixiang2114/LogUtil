package com.bfw.log.constant;

/**
 * @author Lixiang
 * @description 数值单位
 */
public enum ValueUnit {
	/**
	 * 位
	 */
	Bit,
	
	/**
	 * 位/秒
	 */
	BitPerSecond,
	
	/**
	 * 百分比
	 */
	Percentage,
	
	/**
	 * 字节
	 */
	Byte,
	
	/**
	 * 字节/秒
	 */
	BytePerSecond,
	
	/**
	 * 摄氏度
	 */
	Centigrade,
	
	/**
	 * 个
	 */
	Count,
	
	/**
	 * 个/秒
	 */
	CountPerSecond,
	
	/**
	 * 圈\轮
	 */
	Round,
	
	/**
	 * 圈\轮/分
	 */
	RoundPerMinute,
	
	/**
	 * 秒
	 */
	Second,
	
	/**
	 * 毫秒
	 */
	Millisecond,
	
	/**
	 * 其它
	 */
	Other
	;
}
